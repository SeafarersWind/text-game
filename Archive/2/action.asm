  ACdSIZE  equ 16	;address al $00..$0F
  ACdCOUNT equ 256	;address ax $000-..$0FF-
  
  ACMSG   equ 0 ;2 bytes
  
  times ACdSIZE*ACdCOUNT db 0
 
